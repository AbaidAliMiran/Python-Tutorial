# How do you write a program that checks if a given character is vowel or not using if _else?
ch = input("Enter any character:")
if ch == 'a' or ch == 'A' or ch == 'e' or ch == 'E' or ch == 'o' or ch == 'O' or ch == 'i' or ch == 'I' or ch == 'u' or ch == 'U':
    print(ch, " is vowel.")
else:
    print(ch, " is not vowel.")